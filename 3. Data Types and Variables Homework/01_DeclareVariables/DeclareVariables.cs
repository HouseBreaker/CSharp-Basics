using System;

	class DeclareVariables
	{
		static void Main()
		{
			// 52130, -115, 4825932, 97, -10000
			ushort ushort_ex = 52130;
			sbyte sbyte_ex = -115;
			int int_ex = 4825932;
			byte byte_ex = 97;
			short short_ex = -10000;

			Console.WriteLine("ushort example  {0}", ushort_ex);
			Console.WriteLine("sbyte example   {0}", sbyte_ex);
			Console.WriteLine("int example     {0}", int_ex);
			Console.WriteLine("byte example    {0}", byte_ex);
			Console.WriteLine("short example   {0}\n", short_ex);

		}
	}
